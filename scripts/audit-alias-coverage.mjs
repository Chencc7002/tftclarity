import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import {
  buildItemCatalogFromItemsResponse,
  buildTraitCatalogFromCompsData,
  buildUnitCatalogFromCompsData,
  mergeCatalogItems,
  mergeCatalogTraits,
  mergeCatalogUnits
} from "../src/index.js";

const DEFAULT_PROBE_DIR = ".probe";
const DEFAULT_LIMIT = 12;

function parseArgs(argv) {
  const options = {
    probeDir: DEFAULT_PROBE_DIR,
    limit: DEFAULT_LIMIT,
    writePath: null,
    json: false
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--probe-dir" && argv[index + 1]) {
      options.probeDir = argv[index + 1];
      index += 1;
    } else if (arg.startsWith("--probe-dir=")) {
      options.probeDir = arg.slice("--probe-dir=".length);
    } else if (arg === "--limit" && argv[index + 1]) {
      options.limit = Number(argv[index + 1]);
      index += 1;
    } else if (arg.startsWith("--limit=")) {
      options.limit = Number(arg.slice("--limit=".length));
    } else if (arg === "--write" && argv[index + 1]) {
      options.writePath = argv[index + 1];
      index += 1;
    } else if (arg.startsWith("--write=")) {
      options.writePath = arg.slice("--write=".length);
    } else if (arg === "--json") {
      options.json = true;
    }
  }

  if (!Number.isInteger(options.limit) || options.limit < 0) options.limit = DEFAULT_LIMIT;
  return options;
}

async function readJsonIfExists(filePath) {
  try {
    return JSON.parse(await readFile(filePath, "utf8"));
  } catch (error) {
    if (error.code === "ENOENT") return null;
    throw error;
  }
}

function hasManualAlias(record) {
  const source = String(record.source ?? "");
  return Boolean(record.zhName || record.shortName || record.displayName)
    && (source.includes("alias_override") || source.includes("seed") || record.aliasSource);
}

function tokenFromApiName(apiName) {
  return String(apiName ?? "")
    .replace(/^TFT\d*_Item_/, "")
    .replace(/^TFT_Item_/, "")
    .replace(/^TFT\d*_/, "")
    .replace(/_[0-9]+$/, "");
}

function summarizeRecord(record, type) {
  return {
    type,
    apiName: record.apiName,
    filterId: record.filterId,
    category: record.category,
    source: record.source,
    token: tokenFromApiName(record.filterId ?? record.apiName),
    aliases: (record.aliases ?? []).slice(0, 5)
  };
}

function createCoverage(records, type, options = {}) {
  const candidates = records
    .filter((record) => !options.filter || options.filter(record))
    .map((record) => summarizeRecord(record, type));
  const missing = candidates.filter((record) => {
    const original = records.find((entry) => (
      type === "trait" ? entry.filterId === record.filterId : entry.apiName === record.apiName
    ));
    return !hasManualAlias(original);
  });
  return {
    total: candidates.length,
    covered: candidates.length - missing.length,
    missing
  };
}

function jsString(value) {
  return JSON.stringify(value);
}

function renderUnitCandidate(record) {
  return [
    "  {",
    `    apiName: ${jsString(record.apiName)},`,
    "    zhName: null,",
    `    aliases: [${jsString(record.token)}],`,
    "    confidence: 0.4,",
    "    source: \"coverage_audit_candidate\"",
    "  }"
  ].join("\n");
}

function renderTraitCandidate(record) {
  return [
    "  {",
    `    apiName: ${jsString(String(record.filterId).replace(/_[0-9]+$/, ""))},`,
    `    filterId: ${jsString(record.filterId)},`,
    "    zhName: null,",
    "    displayName: null,",
    `    aliases: [${jsString(record.token)}],`,
    "    confidence: 0.4,",
    "    source: \"coverage_audit_candidate\"",
    "  }"
  ].join("\n");
}

function renderItemCandidate(record) {
  return [
    "  {",
    `    apiName: ${jsString(record.apiName)},`,
    "    zhName: null,",
    "    shortName: null,",
    `    aliases: [${jsString(record.token)}],`,
    "    confidence: 0.4,",
    "    source: \"coverage_audit_candidate\"",
    "  }"
  ].join("\n");
}

function renderCandidateDraft(report) {
  const units = report.units.missing.map(renderUnitCandidate).join(",\n");
  const traits = report.traits.missing.map(renderTraitCandidate).join(",\n");
  const items = report.items.missing.map(renderItemCandidate).join(",\n");
  return [
    "// Generated by npm run audit:aliases -- --write <file>",
    "// Fill zhName/shortName/displayName and verify aliases manually before moving entries into override files.",
    "",
    "export const CANDIDATE_UNIT_ALIAS_OVERRIDES = [",
    units,
    "];",
    "",
    "export const CANDIDATE_TRAIT_ALIAS_OVERRIDES = [",
    traits,
    "];",
    "",
    "export const CANDIDATE_ITEM_ALIAS_OVERRIDES = [",
    items,
    "];",
    ""
  ].join("\n");
}

function printHumanReport(report, limit) {
  const lines = [
    "Alias coverage audit",
    `source=${report.source.probeDir}`,
    `units: ${report.units.covered}/${report.units.total} covered, missing=${report.units.missing.length}`,
    `traits: ${report.traits.covered}/${report.traits.total} covered, missing=${report.traits.missing.length}`,
    `items: ${report.items.covered}/${report.items.total} covered, missing=${report.items.missing.length}`,
    ""
  ];

  for (const [label, section] of [
    ["Missing units", report.units],
    ["Missing traits", report.traits],
    ["Missing items", report.items]
  ]) {
    lines.push(`${label} (first ${Math.min(limit, section.missing.length)}):`);
    for (const record of section.missing.slice(0, limit)) {
      lines.push(`- ${record.filterId ?? record.apiName} token=${record.token} source=${record.source}`);
    }
    lines.push("");
  }

  if (report.source.missingFiles.length) {
    lines.push(`missing probe files: ${report.source.missingFiles.join(", ")}`);
  }

  console.log(lines.join("\n").trimEnd());
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const probeDir = resolve(options.probeDir);
  const files = {
    latestClusterInfo: resolve(probeDir, "meta_latest_cluster.json"),
    compOptions: resolve(probeDir, "meta_comp_options.json"),
    items: resolve(probeDir, "meta_items_expanded.json"),
    legacyItems: resolve(probeDir, "meta_items.json")
  };

  const latestClusterInfo = await readJsonIfExists(files.latestClusterInfo);
  const compOptions = await readJsonIfExists(files.compOptions);
  const itemsResponse = await readJsonIfExists(files.items);
  const legacyItemsResponse = await readJsonIfExists(files.legacyItems);
  const missingFiles = Object.entries({
    latestClusterInfo,
    compOptions,
    itemsResponse,
    legacyItemsResponse
  })
    .filter(([, value]) => !value)
    .map(([key]) => key);

  const compsData = {
    latestClusterInfo: latestClusterInfo ?? {},
    compOptions: compOptions ?? {}
  };
  const units = buildUnitCatalogFromCompsData(compsData);
  const traits = buildTraitCatalogFromCompsData(compsData);
  const items = mergeCatalogItems(
    buildItemCatalogFromItemsResponse(itemsResponse ?? {}),
    buildItemCatalogFromItemsResponse(legacyItemsResponse ?? {})
  );

  const report = {
    source: {
      probeDir,
      missingFiles
    },
    units: createCoverage(units, "unit", {
      filter: (record) => /^TFT17_/.test(record.apiName) && !/Enemy|PVE|Summon$|IvernMinion/i.test(record.apiName)
    }),
    traits: createCoverage(traits, "trait", {
      filter: (record) => /^TFT17_/.test(record.filterId ?? record.apiName)
    }),
    items: createCoverage(items, "item", {
      filter: (record) => !["component", "consumable", "removed_or_legacy"].includes(record.category)
    })
  };

  if (options.writePath) {
    const writePath = resolve(options.writePath);
    await writeFile(writePath, renderCandidateDraft(report), "utf8");
    report.source.writePath = writePath;
  }

  if (options.json) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    printHumanReport(report, options.limit);
    if (options.writePath) console.log(`\nwrote ${report.source.writePath}`);
  }
}

await main();
